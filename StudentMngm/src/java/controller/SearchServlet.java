    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("search.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String cond=request.getParameter("sCriteria");
            String value=request.getParameter("sValue");
            StudentList sList=(StudentList)request.getServletContext().getAttribute("sList");
            ArrayList<Student> rList=null;
            switch(cond){
                case "sid": rList=sList.search(s->s.getId()==Integer.parseInt(value));break;
                case "name":rList=sList.search(s->s.getName().indexOf(value)>=0);break;
                case "sex": rList=sList.search(s->s.getGender().equals(value));break;
                case "dob": rList=sList.search(s->s.getDob().equals(value));break;
                case "all": rList=sList.getAll();
            }
            request.getSession().setAttribute("sList", rList);
            request.getRequestDispatcher("searchList.jsp").forward(request, response);

    }

